    br2,
    muxA,
    ld,
    st,
    abs,
    ioW,
    ioR,
    stPC,
    Reti,
    flash_req,
    BRK
} = ctrl;

always @(*) begin
    case (opcode)
        7'b0000000: ctrl = 25'b000_00000_000_0000_0000_00000_0;//8'hx00:
        7'b0000001: ctrl = 25'b000_00000_100_0000_1000_00000_0;//8'hx01:

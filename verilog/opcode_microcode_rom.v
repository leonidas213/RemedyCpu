module opcode_microcode_rom(
    input  [6:0] opcode,
    output muxb0,
    output muxb1,
    output muxb2,
    output aluop0,
    output aluop1,
    output aluop2,
    output aluop3,
    output aluop4,
    output WE,
    output sf,
    output iem0,
    output iem1,
    output br0,
    output br1,
    output br2,
    output muxA,
    output ld,
    output st,
    output abs,
    output ioW,
    output ioR,
    output stPC,
    output Reti,
    output flash_req,
    output BRK
